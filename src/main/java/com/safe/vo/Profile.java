package com.safe.vo;

public class Profile {
	private String id;
	private String img;
	private String fortune;
	public Profile() {
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getFortune() {
		return fortune;
	}
	public void setFortune(String fortune) {
		this.fortune = fortune;
	}
	
}
